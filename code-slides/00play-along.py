## <p>If you want to play along with the demos, do this:</p>
## <ul>
##   <li><code>git clone https://thula.meejah.ca/pyyyc-pytest-selenium</code></li>
##   <li><code>cd pyyc-pytest-selenium</code></li>
##   <li><code>virtualenv venv</code></li>
##   <li><code>source venv/bin/activate</code></li>
##   <li><code>pip install --editable .</code></li>
## </ul>
## <p>Confirm you have both Firefox and Chrome installed. Confirm that <code>chromedriver</code> is in your path (should have been installed as the last step above): ``which chromedriver`` should print out a thing.</p>
